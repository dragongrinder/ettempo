let character: UInt8 = 0x26

switch character {
case 0x26:
    // Handle the ampersand character (&)
    print("Ampersand (&) character detected")
case 0x41:
    // Handle the character 'A'
    print("Character A detected")
case 0x42:
    // Handle the character 'B'
    print("Character B detected")
default:
    print("Other character")
}
